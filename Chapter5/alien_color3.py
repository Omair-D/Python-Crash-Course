# Create variable alien_color and assign a color
alien_color = 'green'

# Write an if-elif-else chain printing for each color

# 5 points elif chain
if alien_color == 'green':
	print('You have earned 5 points!')
elif alien_color == 'yellow':
	print('You have earned 10 points!')
else:
	print('You have earned 15 points')

# 10 points elif chain
alien_color = 'yellow'

if alien_color == 'green':
	print('You have earned 5 points!')
elif alien_color == 'yellow':
	print('You have earned 10 points!')
else:
	print('You have earned 15 points')

